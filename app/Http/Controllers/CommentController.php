    {
        Auth::guard()->login(User::find(1));

        $msg = $request->session()->pull('msg', '');

        return view('comment.index')->with([
            'comments' => Comment::with('user')->orderBy('created_at', 'desc')->paginate(5),
            'msg' => $msg,
